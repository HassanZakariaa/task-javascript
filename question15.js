let x=parseInt(prompt('Enter the number 1 = '))
let y=parseInt(prompt('Enter the Number 2 = '))
let  options=prompt('Enter the option')
switch(options){
    case'+':
    document.write(x+y)
    break;
    case'-':
    document.write(x-y)
    break;
    case'*':
    document.write(x*y)
    break;
    case'/':
    document.write(x/y)
    break;
    
}